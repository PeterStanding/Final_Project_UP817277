    private void subButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_subButtonActionPerformed
        nameIncorrect.hide();
        passwordIncorrect.hide();
        schoolIncorrect.hide();
        
        i = 0;
        nameCorr = false;
        passCorr= false;
        schoolCorr = false;
        stuName = name.getText();
        stuPass = password.getText();
        stuSchool = school.getText();
        
        ArrayList <String> nameList = keyFunctions.readFile("db/names.txt");        
        ArrayList <String> passList = keyFunctions.readFile("db/passwords.txt");
        ArrayList <String> schoolList = keyFunctions.readFile("db/schools.txt");
        
        
        if (nameList.contains(stuName)){
            nameCorr = true;
            }else{
            nameIncorrect.show();
            nameIncorrect.setText("Name Incorrect");
        }
        if (nameCorr){
            i = nameList.indexOf(stuName);
        }
        
        if ((passList.get(i).equals(stuPass))){
            passCorr = true;
            }else{
            passwordIncorrect.show();
            passwordIncorrect.setText("Password Incorrect");
        }
        
        if (schoolList.get(i).equals(stuSchool)){
            schoolCorr = true;
            }else{
            schoolIncorrect.show();
            schoolIncorrect.setText("School Incorrect");
        }
        
        if (nameCorr && passCorr && schoolCorr){
           homePage.main(stuName, stuSchool);
           this.hide(); 
        }
    }//GEN-LAST:event_subButtonActionPerformed

